const replacementPairs = Object.entries(kannadaDictionary).sort(
  ([left], [right]) => right.length - left.length
);

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function applyDictionary(input: string): string {
  let output = input;
  for (const [source, target] of replacementPairs) {
    const regex = new RegExp(escapeRegExp(source), "gi");
    output = output.replace(regex, target);
  }
  return output;
}

export function translateText(input: string, language: Language): string {
  if (language === "en") {
    return input;
  }
  return applyDictionary(input);
}
